#for loop in python
for i in range(1, 10):
    print(i)

#while loop in python
num = 0
while num < 4 :
    print(num)
    num +=  1

# Nested Loop 
for i in range(3):
    for j in range(3):
        print(f"({i}, {j})", end=" ")
    print()

